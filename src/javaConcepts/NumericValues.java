package javaConcepts;

public class NumericValues {

	public static void main(Strings[] args) {

		byte bt = 127;
		System.out.println(bt);
		
		short st = 32767;
		System.out.println(st);
		
		int it = 100000;
		System.out.println(it);
		
		long lg = 1000000000000l;
		System.out.println(lg);
	}

}
