package javaConcepts;

public class superKeywordConcept_A {
	
	public superKeywordConcept_A () {
		System.out.println("Parent Class");
	}
	
	public superKeywordConcept_A (int i) {
		System.out.println(i);
	}
	public superKeywordConcept_A (int i, int j) {
		System.out.println(i);
		System.out.println(j);
	}
}
